DELIMITER //
CREATE OR REPLACE FUNCTION FN_CREATE_USER
(
	RUT         INT,
    NAME        VARCHAR(60),
    LASTNAME    VARCHAR(60),
    USERNAME    VARCHAR(255),
	MAIL		VARCHAR(255),
    CAREER      INT,
    PHONE       VARCHAR(12),
    ADDRESS     VARCHAR(255),
    PASS        VARCHAR(255)
)
RETURNS INT
BEGIN
	DECLARE X INT;
	SET X = (SELECT COUNT(RUT) FROM USERS WHERE USERS.RUT = RUT AND USERS.USERNAME = LOWER(USERNAME));
	IF(X = 0) THEN
		INSERT INTO USERS values (RUT,NAME,LASTNAME,USERNAME,MAIL,1,1,CAREER,PHONE,ADDRESS,PASS);
		RETURN 1;
	ELSE
		RETURN 2;
	END IF;
END
//

DELIMITER //
CREATE OR REPLACE FUNCTION FN_UPDATE_USER
(
	RUT         INT,
    NAME        VARCHAR(60),
    LASTNAME    VARCHAR(60),
    USERNAME    VARCHAR(255),
	MAIL		VARCHAR(255),
    CAREER      INT,
    PHONE       VARCHAR(12),
    ADDREES     VARCHAR(255),
    PASS        VARCHAR(255)
)
RETURNS INT
BEGIN
	DECLARE X INT;
	SET X = (SELECT COUNT(RUT) FROM USERS WHERE USERS.RUT = RUT);
	IF X = 1 THEN
		UPDATE USERS SET 
		NAME  = NAME,
		LASTNAME = LASTNAME,
		USERNAME = USERNAME,
		CAREER = CAREER,
		PHONE = PHONE,
		ADDREES = ADDREES,
		MAIL = MAIL,
		PASS = PASS
		WHERE USERS.RUT = RUT;
		RETURN 1;
	ELSE
		RETURN 2;
	END IF;
end
