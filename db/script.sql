DELIMITER //
CREATE OR REPLACE FUNCTION FN_CREATE_PRODUCT
(
	NAME            VARCHAR(255),
    DESCRIPTION     VARCHAR(255),
    STOCK           INT,
    PRICE           INT,
    STORE           INT
)
RETURNS INT
BEGIN
    INSERT INTO PRODUCT(NAME, DESCRIPTION, STATUS, STOCK, PRICE, STORE) VALUES(NAME,DESCRIPTION,1,STOCK,PRICE,STORE);
    RETURN 1;
end
//

DELIMITER //
CREATE OR REPLACE FUNCTION FN_UPDATE_PRODUCT
(
	ID				INT,
	NAME            VARCHAR(255),
    DESCRIPTION     VARCHAR(255),
    STOCK           INT,
    PRICE           INT,
    STORE           INT
)
RETURNS INT
BEGIN
    DECLARE X INT;
	SET X = (SELECT COUNT(ID) FROM PRODUCT WHERE PRODUCT.ID = ID);
	IF X = 1 THEN
		UPDATE PRODUCT SET
		NAME = NAME,
		DESCRIPTION = DESCRIPTION,
		STOCK = STOCK,
		PRICE = PRICE,
		STORE = STORE
		WHERE PRODUCT.ID = ID;
		RETURN 1;
	ELSE 
		RETURN 2;
	END IF;
end
//

