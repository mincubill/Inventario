DELIMITER //
CREATE OR REPLACE PROCEDURE SP_LOGIN
(
	IN  VUSERNAME	VARCHAR(255),
	IN	VPASS		VARCHAR(255)
)
BEGIN
	SELECT NAME,TYPE FROM USERS WHERE USERNAME = LOWER(VUSERNAME) AND PASS = VPASS;
END
